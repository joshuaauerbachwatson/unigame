import Foundation

// Alternate UserDefaults for testing
// https://stackoverflow.com/questions/65191363/userdefaults-instance-for-unit-testing

class MockDefaults : UserDefaults {
    convenience init() {
        self.init(suiteName: "MockDefaults")!
    }
    
    override init?(suiteName suitename: String?) {
        UserDefaults().removePersistentDomain(forName: suitename!)
        super.init(suiteName: suitename)
    }
}
